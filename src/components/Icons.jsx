export function Badge({ type, label }) {
  const map = {
    Accept: ['badge-accept', 'Accepted'],
    Reject: ['badge-reject', 'Rejected'],
    Done: ['badge-done', 'Done'],
    'In Progress': ['badge-progress', 'In Progress'],
    Pending: ['badge-pending', 'Pending'],
    satisfied: ['badge-accept', 'Satisfied ✓'],
    not_satisfied: ['badge-reject', 'Disputed ✗'],
    '': ['badge-none', 'No status'],
  }
  const [cls, text] = map[type] ?? ['badge-none', type]
  return <span className={`badge ${cls}`}>{label ?? text}</span>
}